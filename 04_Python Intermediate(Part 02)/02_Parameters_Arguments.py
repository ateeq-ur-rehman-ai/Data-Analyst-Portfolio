'''
Parameters are the variables written inside the parentheses with name of function.
Arguments are the value passed to the parameters while calling the function.
'''

def multiply(a,b):
    print(a*b)
multiply(3,7)

def Divide(a,b):
    print(a/b)
Divide(81,9)

def Area(length,width):
    print("The area of rectangle is :" , length*width)
Area(6,3)

