name=["Qasim","Rehman","Asim","Munir","Kamran","Asim"]
print(name)

# To find the length of list 
print(len(name))

# To count the occurance of particular element 
print(name.count("Asim"))

# To add to the list
name.append("Zain")
print(name)

# To add element in specific location
name.insert(4,"Ammar")
print(name)

# To remove a value from list 
name.remove("Zain")
print(name)

# To remove a value form certain location from the list 
name.pop(3)
print(name)