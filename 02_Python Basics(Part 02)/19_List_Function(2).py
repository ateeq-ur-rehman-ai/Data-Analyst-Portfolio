name=["Qasim","Rehman","Asim","Munir","Kamran","Asim"]
print(name)

# To create a ccopy of list 
a=[]
print(a)
a=name.copy()
print(a)

# To access an index of an element 
print(name.index("Munir"))

# To extend the list 
name_1= ["Haris","Sufiyan","Talha"]
name.extend(name_1)
print(name)

# To reverse the list 
name.reverse()
print(name)

# To sort the list from  A to Z
name.sort()
print(name)

# To clear all the data from the list 
name.clear()
print(name)