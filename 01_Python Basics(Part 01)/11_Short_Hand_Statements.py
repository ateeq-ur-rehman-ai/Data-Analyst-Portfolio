'''
Short hand if statement is used when only one statement needs to be executed inside the if block the statement can 
be mentioned in the same line which holds if statement  
'''
age = 20
if age >= 18: print("You are an adult.")

'''
If-else statement in one line in which there is only one statement to execute in both if and else block.    
'''
marks = 75
print("Pass") if marks >= 50 else print("Fail")


