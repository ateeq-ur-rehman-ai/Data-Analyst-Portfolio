# TYPE CASTING AND ITS TYPES

'''
Conversion of one data type into another data type is called type casting .
1.Implicit type casting : Where python itself convert onedata type to another .
2.Explicit type casting : Where user convert onedata type to another datatype .
'''

a=573
b=4.27
c=a+b
print(c)
print(type(c))
d="78"
e=28
d=int(d)
f=d+e
print(f)
print(type(f))